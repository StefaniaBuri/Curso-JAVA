public class Car {

    String model;
    String color;
    int seats;
    int numDoor;
    boolean isAuto;
    char type;

    Car(String model, String color, int seats, int numDoor, boolean isAuto, char type) {
        this.model = model;
        this.color = color;
        this.seats = seats;
        this.numDoor = numDoor;
        this.isAuto = isAuto;
        this.type = type;
    }
}
